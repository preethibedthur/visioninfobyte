    // to display issued book details in table
    private void showIssuedBook(){
        try{
            DBconnection ob= new DBconnection();
            ResultSet rs =ob.st.executeQuery("select * from issued_book");
        
            while(rs.next()){
                
                int issId=rs.getInt("issueId");
                bookId=rs.getInt("bookId");
                bookName=rs.getString("bookName");
                memId=rs.getInt("memId");
                memName=rs.getString("memName");
                java.sql.Date issDate=rs.getDate("issueDate");
                java.sql.Date dueDate=rs.getDate("dueDate");
            
                Object[] rowData={issId, bookId,bookName,memId,memName,issDate,dueDate};
                DefaultTableModel model= (DefaultTableModel)issueTable.getModel();
                model.addRow(rowData); 
        }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
    //to clear table
    private void clearTable(){
        DefaultTableModel model= (DefaultTableModel)issueTable.getModel();
        model.setRowCount(0);
    }
    
    //to insert isuued book details in database 
    private boolean issueBook(){
        boolean isIssued=false;

        bookId=Integer.parseInt(bookIdTxt.getText());
        memId=Integer.parseInt(memIdTxt.getText());
        bookName= bookNameLbl.getText();
        memName=memNameLbl.getText();
        
        
        Long d1= issDate.getDatoFecha().getTime();
        Long d2= retDate.getDatoFecha().getTime();
        
        java.sql.Date sIssueDate = new java.sql.Date(d1);
        java.sql.Date sReturnDate = new java.sql.Date(d2);
        
        try{
            DBconnection ob= new DBconnection();
            String sql="insert into issued_book(bookId,bookName,memId,"
                    +"memName, issueDate, dueDate)values(?,?,?,?,?,?)";
            PreparedStatement pst=ob.con.prepareStatement(sql);
            
            pst.setInt(1,bookId);
            pst.setString(2, bookName);
            pst.setInt(3,memId );
            pst.setString(4, memName);
            pst.setDate(5, sIssueDate);
            pst.setDate(6, sReturnDate);
            
            int rowCnt = pst.executeUpdate();
            if(rowCnt>0){
                isIssued=true;
            }

        }
        catch(Exception e){
            e.printStackTrace();
        }        
        
        return isIssued;
    }
    
    // to update the book quantity
    private void updateBookCount(){
        
        bookId=Integer.parseInt(bookIdTxt.getText());
        int quantity = Integer.parseInt(quantityLbl.getText());
        
        try{
            DBconnection ob= new DBconnection();
            String sql="update books set quantity = quantity - 1 where bookId = ?";
            PreparedStatement pst=ob.con.prepareStatement(sql);
            
            pst.setInt(1,bookId);
            
            int rowCount= pst.executeUpdate();
            if(rowCount>0){
                quantityLbl.setText(Integer.toString(quantity-1));
            }
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }
    
    //to check if the book is already issued to the same member
    private boolean isAlreadyIssued(){
        boolean isIssued=false;
        
        bookId=Integer.parseInt(bookIdTxt.getText());
        memId=Integer.parseInt(memIdTxt.getText());
        
        try{
            DBconnection ob= new DBconnection();
            String sql="select * from issued_book where bookId = ? and memId = ?";
            PreparedStatement pst=ob.con.prepareStatement(sql);
            
            pst.setInt(1,bookId);
            pst.setInt(2,memId );
            
            ResultSet rs=pst.executeQuery();
             if(rs.next()){
                 isIssued=true;
             }

        }
        catch(Exception e){
            e.printStackTrace();
        }
        
        
        return isIssued;
    }
    
